import random

def marca_tentativa_correta(tentativa, letras_acertadas, palavra_secreta):
    for index, letra in enumerate(palavra_secreta):
        if (tentativa == letra):
            letras_acertadas[index] = letra

def jogar():

    imprime_mensagem_abertura()
    palavra_secreta = carrega_palavra_secreta()
    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)
    
    enforcou = False
    acertou = False
    erros = 0

    print(letras_acertadas)

    # enquanto não enforcou e não acertou
    while ((not enforcou) and (not acertou)):

        tentativa = pede_tentativa()

        if (tentativa in palavra_secreta):
            marca_tentativa_correta(tentativa, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = (erros == 7)
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if (acertou):
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)
    
    print("Fim do jogo")



def imprime_mensagem_abertura():
    print("*********************************")
    print("***Bem vindo ao jogo da Forca!***")
    print("*********************************")

def carrega_palavra_secreta():
    arquivo = open(r"c:\Users\henrique.rosa\Documents\SEMAV\Cursos\python-avancando-na-linguagem\jogos\palavras.txt", "r")

    palavras = []
    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)
    arquivo.close()

    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero].lower()
    return palavra_secreta

def inicializa_letras_acertadas(palavra):
    return ["_" for letra in palavra]

def pede_tentativa():
        tentativa = input("Qual letra? ")
